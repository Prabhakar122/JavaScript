/********Switch statement********* */

const day = "Wed"
switch (day) {
    case "Sun":
        console.log("Sunday");
        break;
    case "Mon":
        console.log("Monday");
        break;
    case "Tue":
        console.log("Tuesday");
        break;
    case "Wed":
        console.log("Wednesday");
        break;
        default:
        console.log("This is the default condition");
        break;
}